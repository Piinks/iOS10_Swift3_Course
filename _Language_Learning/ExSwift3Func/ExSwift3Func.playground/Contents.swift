import UIKit

func add(a : Double, b : Double) -> Double {
    return (a + b)
}

func subtract(a : Int, b: Int) -> Int {
    return (a - b)
}

func multiply( a : Float, b : Float) -> Float {
    return (a * b)
}

func divide( a: Double, b: Double) -> Double {
    return (a / b)
}

